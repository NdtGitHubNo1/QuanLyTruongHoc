CREATE VIEW [dbo].[XEM_TIEN_THANG]
AS SELECT tblgiaovien.MAGV,CONVERT(VARCHAR,(MONTH(NgayDay))) + '/' + CONVERT(VARCHAR,(YEAR(NgayDay))) AS THANG, (tblGiaoVien.LuongCung) + SUM(SoTienMotTiet * SOTIET * DinhMuc) as LuongGiaoVien
FROM tblCTGD INNER JOIN tblGiaoVien
ON tblCTGD.MaGV = tblCTGD.MaGV
INNER JOIN tblTrinhDo
ON tblGiaoVien.MaTrinhDo = tblTrinhDo.MaTrinhDo
GROUP BY CONVERT(VARCHAR,(MONTH(NgayDay))) + '/' + CONVERT(VARCHAR,(YEAR(NgayDay))), LuongCung,tblgiaovien.MAGV
